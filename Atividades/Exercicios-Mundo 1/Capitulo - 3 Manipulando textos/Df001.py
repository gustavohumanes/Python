frase = "Meu pau tem 15cm"

print(frase) #Demonstra a frase em sí

print(len(frase[2:6])) #O método "len()" Demonstra o comprimento de uma frase

print(frase.count("pau")) #O método "count()" Demonstra quantas vezes alguma letra/palavra apareceu em uma string

print(frase.find("Meu")) #O método "len()" Demonstra a posição que alguma letra/palavra COMEÇA, nesse caso, "Meu" começa em [0]

print("Meu" in frase) #Nesse caso, estamos verificando se "Meu" existe em alguma parte de frase, retornando "False" ou "True"